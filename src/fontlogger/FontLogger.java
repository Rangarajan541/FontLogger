    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        try {
            File target = new File(jTextField3.getText());
            if (target.exists() || target.createNewFile()) {
                BufferedReader br = new BufferedReader(new FileReader(target));
                String dummy;
                ArrayList<String> alreadyExistingFonts = new ArrayList<>();
                ArrayList<String> ignored = new ArrayList<>();
                while ((dummy = br.readLine()) != null) {
                    alreadyExistingFonts.add(dummy);
                }
                BufferedWriter bw = new BufferedWriter(new FileWriter(target, true));
                for (int i = 0; i < jTable2.getRowCount(); i++) {
                    String toWrite = (String) jTable2.getValueAt(i, 0);
                    if (!alreadyExistingFonts.contains(toWrite)) {
                        bw.write(toWrite + System.getProperty("line.separator"));
                    } else {
                        ignored.add(toWrite);
                    }
                }
                bw.flush();
                bw.close();
                confModel.setRowCount(0);
                StringBuffer out = new StringBuffer();
                if (ignored.size() > 0) {
                    out.append("Ignored ").append(ignored.size()).append(" files because they already exist:\n\n");
                    for (String x : ignored) {
                        out.append(x).append("\n");
                    }
                }
                JOptionPane.showMessageDialog(this, "Completed recording. Find your file at:\n" + target.getCanonicalPath() + "\n\n\n" + out, "FontLogger - Complete", JOptionPane.INFORMATION_MESSAGE);
                bw = new BufferedWriter(new FileWriter(settingsFile));
                bw.write(jTextField1.getText().trim() + System.getProperty("line.separator"));
                bw.write(jTextField2.getText().trim() + System.getProperty("line.separator"));
                bw.write(jTextField3.getText().trim() + System.getProperty("line.separator"));
                bw.flush();
                bw.close();
            } else {
                JOptionPane.showMessageDialog(this, "Unable to create file. \nPlease run as administrator or browse to different location.", "FontLogger - Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (IOException e) {
            handle("writing to file.", e);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

