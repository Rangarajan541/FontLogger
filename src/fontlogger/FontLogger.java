    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        formats.clear();
        matchModel.setRowCount(0);
        confModel.setRowCount(0);
        if (jTextField2.getText().trim().length() > 0) {
            formats.addAll(Arrays.asList(jTextField2.getText().trim().split(";")));
        }
        if (formats.size() > 0) {
            try {
                String path = jTextField1.getText().trim();
                if (path.length() > 0) {
                    File folder = new File(path);
                    if (folder.exists()) {
                        for (File temp : folder.listFiles()) {
                            try {
                                String[] tokens = temp.getCanonicalPath().split("\\.");
                                String extension = tokens[tokens.length - 1];
                                if (formats.contains(extension)) {
                                    matchModel.addRow(new Object[]{temp.getName(), extension});
                                }
                            } catch (ArrayIndexOutOfBoundsException e) {
                            }
                        }
                        jLabel5.setText("No. of files found: " + matchModel.getRowCount());
                    } else {
                        JOptionPane.showMessageDialog(this, "That directory does not exist.", "FontLogger - Invalid directory", JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Please select a directory", "FontLogger - No directory selected", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (IOException ex) {
                handle("fetching files.", ex);
            }
        } else {
            JOptionPane.showMessageDialog(this, "You have not added any formats.", "FontLogger - No formats added", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton6ActionPerformed

